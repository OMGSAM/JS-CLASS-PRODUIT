class produit {
    constructor(reference, prix, quantite) {
        this.reference = reference
        this.prix = prix
        this.quantite = quantite
    }
    getreference() {
        return this.reference
    }
    setreference(value) {
        this.reference = value
    }
    getprix() {
        return this.prix
    }
    setprix(value) {
        this.prix = value
    }
    getquantite() {
        return this.quantite
    }
    setquantite(value) {
        this.quantite = value
    }
    Affichage() {
        console.log(this.reference + " " + this.prix + " " + this.quantite)
    }
}
